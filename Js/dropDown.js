function headerMenu() {
    document.getElementById("menu-header").classList.toggle("show");
}
window.onclick = function (event) {
    if (!event.target.matches('.menu-header__btn')) {
        var dropdowns = document.getElementsByClassName("menu-header__dec");
        var i;
        for (i = 0; i < dropdowns.length; i++) {
            var openDropdown = dropdowns[i];
        }
    }
}

